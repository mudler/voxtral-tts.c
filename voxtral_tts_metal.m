/* ========================================================================
 * BF16 -> F16 Weight Cache
 *
 * MPS supports mixed f32/f16 matmul but not f32/bf16. We cache the
 * bf16->f16 conversion per weight pointer (CPU mmap address as key).
 * ======================================================================== */

#define F16_WEIGHT_CACHE_SIZE 512

typedef struct {
    const void *cpu_ptr;
    id<MTLBuffer> gpu_buffer;
    size_t num_elements;
} f16_cache_entry_t;

static f16_cache_entry_t g_f16_cache[F16_WEIGHT_CACHE_SIZE];
static int g_f16_cache_count = 0;
static pthread_mutex_t g_f16_cache_mutex = PTHREAD_MUTEX_INITIALIZER;

static inline uint16_t bf16_to_f16(uint16_t bf16) {
    uint32_t sign = (bf16 >> 15) & 0x1;
    int32_t exp = (bf16 >> 7) & 0xFF;
    uint32_t mant = bf16 & 0x7F;

    if (exp == 0) return (uint16_t)(sign << 15);
    if (exp == 0xFF) return (uint16_t)((sign << 15) | 0x7C00 | (mant ? 0x200 : 0));

    int32_t new_exp = exp - 127 + 15;
    if (new_exp <= 0) return (uint16_t)(sign << 15);
    if (new_exp >= 31) return (uint16_t)((sign << 15) | 0x7C00);

    uint32_t new_mant = mant << 3;
    return (uint16_t)((sign << 15) | (new_exp << 10) | new_mant);
}

static id<MTLBuffer> get_cached_f16_buffer(const uint16_t *weights, size_t num_elements) {
    pthread_mutex_lock(&g_f16_cache_mutex);

    for (int i = 0; i < g_f16_cache_count; i++) {
        if (g_f16_cache[i].cpu_ptr == (const void *)weights) {
            id<MTLBuffer> buf = g_f16_cache[i].gpu_buffer;
            pthread_mutex_unlock(&g_f16_cache_mutex);
            return buf;
        }
    }

    uint16_t *f16_data = (uint16_t *)malloc(num_elements * sizeof(uint16_t));
    for (size_t i = 0; i < num_elements; i++)
        f16_data[i] = bf16_to_f16(weights[i]);

    id<MTLBuffer> buf = [g_device newBufferWithBytes:f16_data
                                              length:num_elements * sizeof(uint16_t)
                                             options:MTLResourceStorageModeShared];
    free(f16_data);

    if (g_f16_cache_count < F16_WEIGHT_CACHE_SIZE) {
        g_f16_cache[g_f16_cache_count].cpu_ptr = (const void *)weights;
        g_f16_cache[g_f16_cache_count].gpu_buffer = buf;
        g_f16_cache[g_f16_cache_count].num_elements = num_elements;
        g_f16_cache_count++;
    }

    pthread_mutex_unlock(&g_f16_cache_mutex);
    return buf;
}

/* ========================================================================
 * F32 Weight/Activation Buffer Cache
 * ======================================================================== */

#define WEIGHT_CACHE_SIZE 256

typedef struct {
    const void *cpu_ptr;
    id<MTLBuffer> gpu_buffer;
    size_t size;
} weight_cache_entry_t;

static weight_cache_entry_t g_weight_cache[WEIGHT_CACHE_SIZE];
static int g_weight_cache_count = 0;

static id<MTLBuffer> get_f32_buffer(const float *data, size_t n_floats) {
    /* Check cache */
    for (int i = 0; i < g_weight_cache_count; i++) {
        if (g_weight_cache[i].cpu_ptr == data)
            return g_weight_cache[i].gpu_buffer;
    }

    /* Upload */
    size_t bytes = n_floats * sizeof(float);
    id<MTLBuffer> buf = [g_device newBufferWithBytes:data
                                              length:bytes
                                             options:MTLResourceStorageModeManaged];

    if (g_weight_cache_count < WEIGHT_CACHE_SIZE) {
        g_weight_cache[g_weight_cache_count].cpu_ptr = data;
        g_weight_cache[g_weight_cache_count].gpu_buffer = buf;
        g_weight_cache[g_weight_cache_count].size = bytes;
        g_weight_cache_count++;
    }

    return buf;
}

/* ========================================================================
 * Shared Memory Allocation (Zero-Copy GPU-CPU)
 * ======================================================================== */

#define SHARED_ALLOC_MAX 16

static struct {
    void *ptr;
    id<MTLBuffer> buf;
} g_shared_allocs[SHARED_ALLOC_MAX];
static int g_shared_count = 0;

void *tts_metal_shared_alloc(size_t size) {
    if (!g_initialized || g_shared_count >= SHARED_ALLOC_MAX)
        return calloc(1, size);

    id<MTLBuffer> buf = [g_device newBufferWithLength:size
                                              options:MTLResourceStorageModeShared];
    if (!buf) return calloc(1, size);

    void *ptr = [buf contents];
    memset(ptr, 0, size);

    g_shared_allocs[g_shared_count].ptr = ptr;
    g_shared_allocs[g_shared_count].buf = buf;
    g_shared_count++;

    return ptr;
}

void tts_metal_shared_free(void *ptr) {
    for (int i = 0; i < g_shared_count; i++) {
        if (g_shared_allocs[i].ptr == ptr) {
            g_shared_allocs[i].buf = nil;
            /* Shift remaining entries */
            for (int j = i; j < g_shared_count - 1; j++)
                g_shared_allocs[j] = g_shared_allocs[j + 1];
            g_shared_count--;
            return;
        }
    }
    free(ptr); /* Not a shared alloc, must be calloc fallback */
}

static id<MTLBuffer> find_shared_buffer(const void *ptr) {
    for (int i = 0; i < g_shared_count; i++) {
        if (g_shared_allocs[i].ptr == ptr)
            return g_shared_allocs[i].buf;
    }
    return nil;
}

/* ========================================================================
 * Activation Buffer Pool (reusable scratch buffers)
 * ======================================================================== */

#define ACTIVATION_POOL_SIZE 32

static struct {
    id<MTLBuffer> buf;
    size_t size;
    int in_use;
} g_activation_pool[ACTIVATION_POOL_SIZE];
static int g_pool_count = 0;

static id<MTLBuffer> pool_get_buffer(size_t size) {
    /* Try to find a reusable buffer of sufficient size */
    for (int i = 0; i < g_pool_count; i++) {
        if (!g_activation_pool[i].in_use && g_activation_pool[i].size >= size) {
            g_activation_pool[i].in_use = 1;
            return g_activation_pool[i].buf;
        }
    }

    /* Allocate new */
    id<MTLBuffer> buf = [g_device newBufferWithLength:size
                                              options:MTLResourceStorageModeShared];
    if (g_pool_count < ACTIVATION_POOL_SIZE) {
        g_activation_pool[g_pool_count].buf = buf;
        g_activation_pool[g_pool_count].size = size;
        g_activation_pool[g_pool_count].in_use = 1;
        g_pool_count++;
    }
    return buf;
}

static void pool_release_buffer(id<MTLBuffer> buf) {
    for (int i = 0; i < g_pool_count; i++) {
        if (g_activation_pool[i].buf == buf) {
            g_activation_pool[i].in_use = 0;
            return;
        }
    }
}

/* ========================================================================
 * Shader Initialization
 * ======================================================================== */

static int init_shaders(void) {
    NSString *shaderSource = [[NSString alloc]
        initWithBytes:voxtral_tts_shaders_metal
               length:voxtral_tts_shaders_metal_len
             encoding:NSUTF8StringEncoding];

    MTLCompileOptions *options = [[MTLCompileOptions alloc] init];
    options.mathMode = MTLMathModeFast;

    NSError *error = nil;
    g_shader_library = [g_device newLibraryWithSource:shaderSource
                                              options:options
                                                error:&error];
    if (!g_shader_library) {
        fprintf(stderr, "metal: shader compilation failed: %s\n",
                [[error description] UTF8String]);
        return -1;
    }

    /* Create pipeline states for each kernel */
    struct { const char *name; id<MTLComputePipelineState> __strong *pipeline; } kernels[] = {
        {"rms_norm", &g_rms_norm_pipeline},
        {"silu", &g_silu_pipeline},
        {"silu_mul", &g_silu_mul_pipeline},
        {"add_inplace", &g_add_inplace_pipeline},
        {"rope_apply", &g_rope_apply_pipeline},
        {"rope_apply_pos", &g_rope_apply_pos_pipeline},
        {"kv_cache_copy", &g_kv_cache_copy_pipeline},
        {"batched_kv_cache_copy", &g_batched_kv_cache_copy_pipeline},
        {"decoder_attention", &g_decoder_attention_pipeline},
        {"causal_softmax", &g_causal_softmax_pipeline},
        {"bf16_to_f32", &g_bf16_to_f32_pipeline},
        {"time_embedding", &g_time_embedding_pipeline},
        {"argmax", &g_argmax_pipeline},
        {NULL, NULL}
    };

    for (int i = 0; kernels[i].name; i++) {
        NSString *name = [NSString stringWithUTF8String:kernels[i].name];
        id<MTLFunction> func = [g_shader_library newFunctionWithName:name];
        if (func) {
            *kernels[i].pipeline = [g_device newComputePipelineStateWithFunction:func
                                                                           error:&error];
            if (!*kernels[i].pipeline) {
                fprintf(stderr, "metal: pipeline creation failed for %s: %s\n",
                        kernels[i].name, [[error description] UTF8String]);
            }
        } else {
            fprintf(stderr, "metal: kernel '%s' not found in shader library\n",
                    kernels[i].name);
        }
    }

    return 0;
}

/* ========================================================================
 * Metal Initialization
 * ======================================================================== */

int tts_metal_init(void) {
    @autoreleasepool {
        g_device = MTLCreateSystemDefaultDevice();
        if (!g_device) {
            fprintf(stderr, "metal: no Metal device found\n");
            return 0;
        }

        /* Check GPU family (require Apple6+ for good performance) */
        if (![g_device supportsFamily:MTLGPUFamilyApple7] &&
            ![g_device supportsFamily:MTLGPUFamilyApple6]) {
            fprintf(stderr, "metal: GPU family too old (need Apple6+)\n");
            return 0;
        }

        g_queue = [g_device newCommandQueue];
        if (!g_queue) {
            fprintf(stderr, "metal: failed to create command queue\n");
            return 0;
        }

        if (init_shaders() != 0) {
            return 0;
        }

        g_initialized = 1;

        if (tts_verbose)
            fprintf(stderr, "  Metal: %s, %.1f GB\n",
                    [[g_device name] UTF8String],
                    (float)[g_device recommendedMaxWorkingSetSize] / (1024*1024*1024));
    }
    return 1;
}

int tts_metal_available(void) {
    return g_initialized;
}

void tts_metal_shutdown(void) {
    if (!g_initialized) return;

    /* Release caches */
    for (int i = 0; i < g_f16_cache_count; i++)
        g_f16_cache[i].gpu_buffer = nil;
    g_f16_cache_count = 0;

    for (int i = 0; i < g_weight_cache_count; i++)
        g_weight_cache[i].gpu_buffer = nil;
    g_weight_cache_count = 0;

    for (int i = 0; i < g_pool_count; i++)
        g_activation_pool[i].buf = nil;
    g_pool_count = 0;

    for (int i = 0; i < g_shared_count; i++)
        g_shared_allocs[i].buf = nil;
    g_shared_count = 0;

    g_dec_x = nil;

    /* Release pipelines */
    g_rms_norm_pipeline = nil;
    g_silu_pipeline = nil;
    g_silu_mul_pipeline = nil;
    g_add_inplace_pipeline = nil;
    g_rope_apply_pipeline = nil;
    g_rope_apply_pos_pipeline = nil;
    g_kv_cache_copy_pipeline = nil;
    g_batched_kv_cache_copy_pipeline = nil;
    g_decoder_attention_pipeline = nil;
    g_causal_softmax_pipeline = nil;
    g_bf16_to_f32_pipeline = nil;
    g_time_embedding_pipeline = nil;
    g_argmax_pipeline = nil;

    g_shader_library = nil;
    g_queue = nil;
    g_device = nil;
    g_initialized = 0;
}

/* ========================================================================
 * MPS Matrix Multiplication
 * ======================================================================== */

void tts_metal_sgemm_bf16(int M, int N, int K,
                           const float *A, const uint16_t *B_bf16, float *C) {
    @autoreleasepool {
        /* Convert bf16 -> f16 for weights (half memory, good enough for most layers).
         * Apple Silicon unified memory means shared buffers are zero-copy. */
        id<MTLBuffer> bufB = get_cached_f16_buffer(B_bf16, (size_t)N * K);

        id<MTLBuffer> bufA = [g_device newBufferWithBytes:A
                                                   length:(size_t)M * K * sizeof(float)
                                                  options:MTLResourceStorageModeShared];
        id<MTLBuffer> bufC = [g_device newBufferWithLength:(size_t)M * N * sizeof(float)
                                                   options:MTLResourceStorageModeShared];

        /* Mixed precision: f32 activations x f16 weights -> f32 output */
        MPSMatrixDescriptor *descA = [MPSMatrixDescriptor
            matrixDescriptorWithRows:M columns:K rowBytes:K * sizeof(float)
            dataType:MPSDataTypeFloat32];
        MPSMatrixDescriptor *descB = [MPSMatrixDescriptor
            matrixDescriptorWithRows:N columns:K rowBytes:K * sizeof(uint16_t)
            dataType:MPSDataTypeFloat16];
        MPSMatrixDescriptor *descC = [MPSMatrixDescriptor
            matrixDescriptorWithRows:M columns:N rowBytes:N * sizeof(float)
            dataType:MPSDataTypeFloat32];

        MPSMatrix *matA = [[MPSMatrix alloc] initWithBuffer:bufA descriptor:descA];
        MPSMatrix *matB = [[MPSMatrix alloc] initWithBuffer:bufB descriptor:descB];
        MPSMatrix *matC = [[MPSMatrix alloc] initWithBuffer:bufC descriptor:descC];

        MPSMatrixMultiplication *mm = [[MPSMatrixMultiplication alloc]
            initWithDevice:g_device
             transposeLeft:NO
            transposeRight:YES
                resultRows:M
             resultColumns:N
           interiorColumns:K
                     alpha:1.0
                      beta:0.0];

        id<MTLCommandBuffer> cmdBuffer = [g_queue commandBuffer];
        [mm encodeToCommandBuffer:cmdBuffer leftMatrix:matA rightMatrix:matB
                     resultMatrix:matC];
        [cmdBuffer commit];
        [cmdBuffer waitUntilCompleted];

        memcpy(C, [bufC contents], (size_t)M * N * sizeof(float));
    }
}

