/* ========================================================================
 * Monolithic LLM Decoder Forward (all 26 layers in one command buffer)
 * ======================================================================== */

void tts_metal_llm_forward(void *ctx_ptr, const float *input_embed,
                            float *out_hidden, int pos) {
    @autoreleasepool {
        tts_ctx_t *ctx = (tts_ctx_t *)ctx_ptr;
        tts_decoder_t *dec = &ctx->decoder;
        int dim = TTS_DEC_DIM;
        int q_dim = TTS_DEC_HEADS * TTS_DEC_HEAD_DIM;
        int kv_dim = TTS_DEC_KV_HEADS * TTS_DEC_HEAD_DIM;
        int hidden = TTS_DEC_HIDDEN;
        float scale = 1.0f / sqrtf((float)TTS_DEC_HEAD_DIM);

        /* Get/create persistent x buffer */
        if (!g_dec_x) {
            g_dec_x = [g_device newBufferWithLength:dim * sizeof(float)
                                            options:MTLResourceStorageModeShared];
        }

        /* Upload input embedding */
        memcpy([g_dec_x contents], input_embed, dim * sizeof(float));

        /* Allocate scratch buffers from pool */
        id<MTLBuffer> bufXnorm = pool_get_buffer(dim * sizeof(float));
        id<MTLBuffer> bufQKV = pool_get_buffer((q_dim + kv_dim + kv_dim) * sizeof(float));
        id<MTLBuffer> bufAttn = pool_get_buffer(q_dim * sizeof(float));
        id<MTLBuffer> bufProj = pool_get_buffer(dim * sizeof(float));
        id<MTLBuffer> bufGate = pool_get_buffer(hidden * sizeof(float));
        id<MTLBuffer> bufUp = pool_get_buffer(hidden * sizeof(float));
        id<MTLBuffer> bufFfnOut = pool_get_buffer(dim * sizeof(float));

        /* Find KV cache shared buffers */
        id<MTLBuffer> gpu_kv_k = find_shared_buffer(ctx->kv_cache_k);
        id<MTLBuffer> gpu_kv_v = find_shared_buffer(ctx->kv_cache_v);

        /* Create single command buffer for ALL 26 layers */
        id<MTLCommandBuffer> cmdBuffer = [g_queue commandBuffer];

        for (int layer = 0; layer < TTS_DEC_LAYERS; layer++) {
            tts_dec_layer_t *l = &dec->layers[layer];

            /* === RMSNorm === */
            id<MTLBuffer> bufNormW = get_f32_buffer(l->attention_norm, dim);
            {
                id<MTLComputeCommandEncoder> enc = [cmdBuffer computeCommandEncoder];
                [enc setComputePipelineState:g_rms_norm_pipeline];
                [enc setBuffer:g_dec_x offset:0 atIndex:0];
                [enc setBuffer:bufNormW offset:0 atIndex:1];
                [enc setBuffer:bufXnorm offset:0 atIndex:2];
                [enc setBytes:&dim length:sizeof(int) atIndex:3];
                float eps = TTS_DEC_NORM_EPS;
                [enc setBytes:&eps length:sizeof(float) atIndex:4];
                [enc dispatchThreadgroups:MTLSizeMake(1, 1, 1)
                    threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];
                [enc endEncoding];
            }

            /* === Q, K, V projections (MPS matmul) === */
            id<MTLBuffer> bufWq = get_cached_f16_buffer(l->wq_bf16, (size_t)q_dim * dim);
            id<MTLBuffer> bufWk = get_cached_f16_buffer(l->wk_bf16, (size_t)kv_dim * dim);
            id<MTLBuffer> bufWv = get_cached_f16_buffer(l->wv_bf16, (size_t)kv_dim * dim);

            /* Q = x_norm @ Wq^T */
            MPSMatrixDescriptor *descX1 = [MPSMatrixDescriptor
                matrixDescriptorWithRows:1 columns:dim rowBytes:dim*sizeof(uint16_t) dataType:MPSDataTypeFloat16];
            MPSMatrix *matXnorm = [[MPSMatrix alloc] initWithBuffer:bufXnorm descriptor:descX1];

            MPSMatrixDescriptor *descWq = [MPSMatrixDescriptor
                matrixDescriptorWithRows:q_dim columns:dim rowBytes:dim*sizeof(uint16_t) dataType:MPSDataTypeFloat16];
            MPSMatrix *matWq = [[MPSMatrix alloc] initWithBuffer:bufWq descriptor:descWq];

            MPSMatrixDescriptor *descQ = [MPSMatrixDescriptor
                matrixDescriptorWithRows:1 columns:q_dim rowBytes:q_dim*sizeof(uint16_t) dataType:MPSDataTypeFloat16];
            MPSMatrix *matQ = [[MPSMatrix alloc] initWithBuffer:bufQKV descriptor:descQ];

            MPSMatrixMultiplication *mmQ = [[MPSMatrixMultiplication alloc]
                initWithDevice:g_device transposeLeft:NO transposeRight:YES
                    resultRows:1 resultColumns:q_dim interiorColumns:dim alpha:1.0 beta:0.0];
            [mmQ encodeToCommandBuffer:cmdBuffer leftMatrix:matXnorm rightMatrix:matWq resultMatrix:matQ];

            /* K = x_norm @ Wk^T */
            MPSMatrixDescriptor *descWk = [MPSMatrixDescriptor
                matrixDescriptorWithRows:kv_dim columns:dim rowBytes:dim*sizeof(uint16_t) dataType:MPSDataTypeFloat16];
            MPSMatrix *matWk = [[MPSMatrix alloc] initWithBuffer:bufWk descriptor:descWk];

            MPSMatrixDescriptor *descK = [MPSMatrixDescriptor
                matrixDescriptorWithRows:1 columns:kv_dim rowBytes:kv_dim*sizeof(float) dataType:MPSDataTypeFloat32];
            MPSMatrix *matK = [[MPSMatrix alloc] initWithBuffer:bufQKV offset:q_dim*sizeof(float) descriptor:descK];

            MPSMatrixMultiplication *mmK = [[MPSMatrixMultiplication alloc]
                initWithDevice:g_device transposeLeft:NO transposeRight:YES
                    resultRows:1 resultColumns:kv_dim interiorColumns:dim alpha:1.0 beta:0.0];
            [mmK encodeToCommandBuffer:cmdBuffer leftMatrix:matXnorm rightMatrix:matWk resultMatrix:matK];

            /* V = x_norm @ Wv^T */
            MPSMatrixDescriptor *descWv = [MPSMatrixDescriptor
                matrixDescriptorWithRows:kv_dim columns:dim rowBytes:dim*sizeof(uint16_t) dataType:MPSDataTypeFloat16];
            MPSMatrix *matWv = [[MPSMatrix alloc] initWithBuffer:bufWv descriptor:descWv];

            MPSMatrixDescriptor *descV = [MPSMatrixDescriptor
                matrixDescriptorWithRows:1 columns:kv_dim rowBytes:kv_dim*sizeof(float) dataType:MPSDataTypeFloat32];
            MPSMatrix *matV = [[MPSMatrix alloc] initWithBuffer:bufQKV offset:(q_dim+kv_dim)*sizeof(float) descriptor:descV];

            MPSMatrixMultiplication *mmV = [[MPSMatrixMultiplication alloc]
                initWithDevice:g_device transposeLeft:NO transposeRight:YES
                    resultRows:1 resultColumns:kv_dim interiorColumns:dim alpha:1.0 beta:0.0];
            [mmV encodeToCommandBuffer:cmdBuffer leftMatrix:matXnorm rightMatrix:matWv resultMatrix:matV];

            /* === RoPE + KV cache + Attention === */
            {
                id<MTLComputeCommandEncoder> enc = [cmdBuffer computeCommandEncoder];

                /* RoPE on Q */
                [enc setComputePipelineState:g_rope_apply_pos_pipeline];
                [enc setBuffer:bufQKV offset:0 atIndex:0];
                int n_heads = TTS_DEC_HEADS;
                int head_dim = TTS_DEC_HEAD_DIM;
                float rope_theta = TTS_ROPE_THETA;
                [enc setBytes:&n_heads length:sizeof(int) atIndex:1];
                [enc setBytes:&head_dim length:sizeof(int) atIndex:2];
                [enc setBytes:&pos length:sizeof(int) atIndex:3];
                [enc setBytes:&rope_theta length:sizeof(float) atIndex:4];
                int tg = MIN(n_heads * head_dim / 2, 256);
                [enc dispatchThreads:MTLSizeMake(n_heads * head_dim / 2, 1, 1)
                    threadsPerThreadgroup:MTLSizeMake(tg, 1, 1)];

                /* RoPE on K */
                [enc setBuffer:bufQKV offset:q_dim * sizeof(float) atIndex:0];
                int n_kv = TTS_DEC_KV_HEADS;
                [enc setBytes:&n_kv length:sizeof(int) atIndex:1];
                [enc dispatchThreads:MTLSizeMake(n_kv * head_dim / 2, 1, 1)
                    threadsPerThreadgroup:MTLSizeMake(MIN(n_kv * head_dim / 2, 256), 1, 1)];

                [enc memoryBarrierWithScope:MTLBarrierScopeBuffers];

                /* Write K to cache */
                if (gpu_kv_k) {
                    [enc setComputePipelineState:g_kv_cache_copy_pipeline];
                    [enc setBuffer:gpu_kv_k offset:0 atIndex:0];
                    [enc setBuffer:bufQKV offset:q_dim * sizeof(float) atIndex:1];
                    int kv_offset = ((int)((size_t)layer * ctx->kv_cache_max + pos) * kv_dim);
                    [enc setBytes:&kv_offset length:sizeof(int) atIndex:2];
                    [enc setBytes:&kv_dim length:sizeof(int) atIndex:3];
                    [enc dispatchThreads:MTLSizeMake(kv_dim, 1, 1)
                        threadsPerThreadgroup:MTLSizeMake(MIN(kv_dim, 256), 1, 1)];

                    /* Write V to cache */
                    [enc setBuffer:gpu_kv_v offset:0 atIndex:0];
                    [enc setBuffer:bufQKV offset:(q_dim + kv_dim) * sizeof(float) atIndex:1];
                    [enc dispatchThreads:MTLSizeMake(kv_dim, 1, 1)
                        threadsPerThreadgroup:MTLSizeMake(MIN(kv_dim, 256), 1, 1)];

                    [enc memoryBarrierWithScope:MTLBarrierScopeBuffers];

                    /* Attention */
                    [enc setComputePipelineState:g_decoder_attention_pipeline];
                    [enc setBuffer:bufQKV offset:0 atIndex:0]; /* Q */
                    size_t layer_kv_offset = (size_t)layer * ctx->kv_cache_max * kv_dim * sizeof(float);
                    [enc setBuffer:gpu_kv_k offset:layer_kv_offset atIndex:1];
                    [enc setBuffer:gpu_kv_v offset:layer_kv_offset atIndex:2];
                    [enc setBuffer:bufAttn offset:0 atIndex:3];
                    [enc setBytes:&n_heads length:sizeof(int) atIndex:4];
                    int n_kv_heads = TTS_DEC_KV_HEADS;
                    [enc setBytes:&n_kv_heads length:sizeof(int) atIndex:5];
                    [enc setBytes:&head_dim length:sizeof(int) atIndex:6];
                    [enc setBytes:&kv_dim length:sizeof(int) atIndex:7];
                    int seq_k = pos + 1;
                    [enc setBytes:&seq_k length:sizeof(int) atIndex:8];
                    [enc setBytes:&scale length:sizeof(float) atIndex:9];
                    int win = 0;
                    [enc setBytes:&win length:sizeof(int) atIndex:10];
                    [enc setBytes:&pos length:sizeof(int) atIndex:11];
                    [enc dispatchThreadgroups:MTLSizeMake(n_heads, 1, 1)
                        threadsPerThreadgroup:MTLSizeMake(128, 1, 1)];
                }

                [enc endEncoding];
            }

            /* === WO projection === */
            id<MTLBuffer> bufWo = get_cached_f16_buffer(l->wo_bf16, (size_t)dim * q_dim);
            MPSMatrixDescriptor *descAttn = [MPSMatrixDescriptor
                matrixDescriptorWithRows:1 columns:q_dim rowBytes:q_dim*sizeof(uint16_t) dataType:MPSDataTypeFloat16];
            MPSMatrix *matAttn = [[MPSMatrix alloc] initWithBuffer:bufAttn descriptor:descAttn];

            MPSMatrixDescriptor *descWo = [MPSMatrixDescriptor
                matrixDescriptorWithRows:dim columns:q_dim rowBytes:q_dim*sizeof(uint16_t) dataType:MPSDataTypeFloat16];
            MPSMatrix *matWo = [[MPSMatrix alloc] initWithBuffer:bufWo descriptor:descWo];

            MPSMatrixDescriptor *descProj = [MPSMatrixDescriptor
                matrixDescriptorWithRows:1 columns:dim rowBytes:dim*sizeof(uint16_t) dataType:MPSDataTypeFloat16];
            MPSMatrix *matProj = [[MPSMatrix alloc] initWithBuffer:bufProj descriptor:descProj];

            MPSMatrixMultiplication *mmWo = [[MPSMatrixMultiplication alloc]
                initWithDevice:g_device transposeLeft:NO transposeRight:YES
                    resultRows:1 resultColumns:dim interiorColumns:q_dim alpha:1.0 beta:0.0];
            [mmWo encodeToCommandBuffer:cmdBuffer leftMatrix:matAttn rightMatrix:matWo resultMatrix:matProj];

            /* === Residual: x += proj === */
            {
                id<MTLComputeCommandEncoder> enc = [cmdBuffer computeCommandEncoder];
                [enc setComputePipelineState:g_add_inplace_pipeline];
                [enc setBuffer:g_dec_x offset:0 atIndex:0];
                [enc setBuffer:bufProj offset:0 atIndex:1];
                [enc setBytes:&dim length:sizeof(int) atIndex:2];
                [enc dispatchThreads:MTLSizeMake(dim, 1, 1)
                    threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];
                [enc endEncoding];
            }

            /* === FFN: RMSNorm + SwiGLU === */
            id<MTLBuffer> bufFfnNormW = get_f32_buffer(l->ffn_norm, dim);
            {
                id<MTLComputeCommandEncoder> enc = [cmdBuffer computeCommandEncoder];
                [enc setComputePipelineState:g_rms_norm_pipeline];
                [enc setBuffer:g_dec_x offset:0 atIndex:0];
                [enc setBuffer:bufFfnNormW offset:0 atIndex:1];
                [enc setBuffer:bufXnorm offset:0 atIndex:2];
                [enc setBytes:&dim length:sizeof(int) atIndex:3];
                float eps = TTS_DEC_NORM_EPS;
                [enc setBytes:&eps length:sizeof(float) atIndex:4];
                [enc dispatchThreadgroups:MTLSizeMake(1, 1, 1)
                    threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];
                [enc endEncoding];
            }

            /* w1 (gate) */
            id<MTLBuffer> bufW1 = get_cached_f16_buffer(l->w1_bf16, (size_t)hidden * dim);
            MPSMatrixDescriptor *descW1 = [MPSMatrixDescriptor
                matrixDescriptorWithRows:hidden columns:dim rowBytes:dim*sizeof(uint16_t) dataType:MPSDataTypeFloat16];
            MPSMatrix *matW1 = [[MPSMatrix alloc] initWithBuffer:bufW1 descriptor:descW1];
            MPSMatrixDescriptor *descGate = [MPSMatrixDescriptor
                matrixDescriptorWithRows:1 columns:hidden rowBytes:hidden*sizeof(uint16_t) dataType:MPSDataTypeFloat16];
            MPSMatrix *matGate = [[MPSMatrix alloc] initWithBuffer:bufGate descriptor:descGate];
            MPSMatrixMultiplication *mmW1 = [[MPSMatrixMultiplication alloc]
                initWithDevice:g_device transposeLeft:NO transposeRight:YES
                    resultRows:1 resultColumns:hidden interiorColumns:dim alpha:1.0 beta:0.0];
            [mmW1 encodeToCommandBuffer:cmdBuffer leftMatrix:matXnorm rightMatrix:matW1 resultMatrix:matGate];

            /* w3 (up) */
            id<MTLBuffer> bufW3 = get_cached_f16_buffer(l->w3_bf16, (size_t)hidden * dim);
            MPSMatrixDescriptor *descW3 = [MPSMatrixDescriptor
                matrixDescriptorWithRows:hidden columns:dim rowBytes:dim*sizeof(uint16_t) dataType:MPSDataTypeFloat16];
            MPSMatrix *matW3 = [[MPSMatrix alloc] initWithBuffer:bufW3 descriptor:descW3];
            MPSMatrixDescriptor *descUp = [MPSMatrixDescriptor
                matrixDescriptorWithRows:1 columns:hidden rowBytes:hidden*sizeof(uint16_t) dataType:MPSDataTypeFloat16];
            MPSMatrix *matUp = [[MPSMatrix alloc] initWithBuffer:bufUp descriptor:descUp];
            MPSMatrixMultiplication *mmW3 = [[MPSMatrixMultiplication alloc]
                initWithDevice:g_device transposeLeft:NO transposeRight:YES
                    resultRows:1 resultColumns:hidden interiorColumns:dim alpha:1.0 beta:0.0];
            [mmW3 encodeToCommandBuffer:cmdBuffer leftMatrix:matXnorm rightMatrix:matW3 resultMatrix:matUp];

            /* Fused SiLU(gate) * up */
            {
                id<MTLComputeCommandEncoder> enc = [cmdBuffer computeCommandEncoder];
                [enc setComputePipelineState:g_silu_mul_pipeline];
                [enc setBuffer:bufGate offset:0 atIndex:0];
                [enc setBuffer:bufUp offset:0 atIndex:1];
                [enc setBytes:&hidden length:sizeof(int) atIndex:2];
                [enc dispatchThreads:MTLSizeMake(hidden, 1, 1)
                    threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];
                [enc endEncoding];
            }

            /* w2 (down) */
            id<MTLBuffer> bufW2 = get_cached_f16_buffer(l->w2_bf16, (size_t)dim * hidden);
            MPSMatrixDescriptor *descW2 = [MPSMatrixDescriptor
                matrixDescriptorWithRows:dim columns:hidden rowBytes:hidden*sizeof(uint16_t) dataType:MPSDataTypeFloat16];
            MPSMatrix *matW2 = [[MPSMatrix alloc] initWithBuffer:bufW2 descriptor:descW2];
            MPSMatrixDescriptor *descFfnOut = [MPSMatrixDescriptor
                matrixDescriptorWithRows:1 columns:dim rowBytes:dim*sizeof(uint16_t) dataType:MPSDataTypeFloat16];
            MPSMatrix *matFfnOut = [[MPSMatrix alloc] initWithBuffer:bufFfnOut descriptor:descFfnOut];
            MPSMatrixMultiplication *mmW2 = [[MPSMatrixMultiplication alloc]
                initWithDevice:g_device transposeLeft:NO transposeRight:YES
                    resultRows:1 resultColumns:dim interiorColumns:hidden alpha:1.0 beta:0.0];
            [mmW2 encodeToCommandBuffer:cmdBuffer leftMatrix:matGate rightMatrix:matW2 resultMatrix:matFfnOut];

            /* Residual: x += ffn_out */
            {
                id<MTLComputeCommandEncoder> enc = [cmdBuffer computeCommandEncoder];
                [enc setComputePipelineState:g_add_inplace_pipeline];
                [enc setBuffer:g_dec_x offset:0 atIndex:0];
                [enc setBuffer:bufFfnOut offset:0 atIndex:1];
                [enc setBytes:&dim length:sizeof(int) atIndex:2];
                [enc dispatchThreads:MTLSizeMake(dim, 1, 1)
                    threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];
                [enc endEncoding];
            }
        } /* end 26-layer loop */

        /* === Final RMSNorm === */
        id<MTLBuffer> bufFinalNorm = get_f32_buffer(dec->norm, dim);
        id<MTLBuffer> bufHidden = pool_get_buffer(dim * sizeof(float));
        {
            id<MTLComputeCommandEncoder> enc = [cmdBuffer computeCommandEncoder];
            [enc setComputePipelineState:g_rms_norm_pipeline];
            [enc setBuffer:g_dec_x offset:0 atIndex:0];
            [enc setBuffer:bufFinalNorm offset:0 atIndex:1];
            [enc setBuffer:bufHidden offset:0 atIndex:2];
            [enc setBytes:&dim length:sizeof(int) atIndex:3];
            float eps = TTS_DEC_NORM_EPS;
            [enc setBytes:&eps length:sizeof(float) atIndex:4];
            [enc dispatchThreadgroups:MTLSizeMake(1, 1, 1)
                threadsPerThreadgroup:MTLSizeMake(256, 1, 1)];
            [enc endEncoding];
        }

        /* === Submit single command buffer & wait === */
        [cmdBuffer commit];
        [cmdBuffer waitUntilCompleted];

        /* Download hidden state */
        memcpy(out_hidden, [bufHidden contents], dim * sizeof(float));

        /* Release pool buffers */
        pool_release_buffer(bufXnorm);
        pool_release_buffer(bufQKV);
        pool_release_buffer(bufAttn);
        pool_release_buffer(bufProj);
        pool_release_buffer(bufGate);
        pool_release_buffer(bufUp);
        pool_release_buffer(bufFfnOut);
        pool_release_buffer(bufHidden);
    } /* @autoreleasepool */
}

